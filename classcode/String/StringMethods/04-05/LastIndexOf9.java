class LastIndexOf9{
	public static void main(String[] args){

		String str="Shashi";

		System.out.println(str.lastIndexOf('h',1));
		System.out.println(str.lastIndexOf('h',2));
		System.out.println(str.lastIndexOf('h',5));
		System.out.println(str.lastIndexOf('h',0));
		System.out.println(str.lastIndexOf('S',0));
		System.out.println(str.lastIndexOf('s',4));
	}
}

